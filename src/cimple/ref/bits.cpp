static uint32 _count_bits(uint32 x)
{
    return
        _bit_count[0x000000FFU & x] +
        _bit_count[0x000000FFU & (x >> 8)] +
        _bit_count[0x000000FFU & (x >> 16)] +
        _bit_count[x >> 24];
}

uint32 lsb_index(uint32 x)
{
    return _count_bits((x & ~(x - 1)) - 1);
}

